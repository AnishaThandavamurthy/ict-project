#heterogeneous list
fruits = ["pear" , "melon" , "orange" , 10 , 20 , 30]
print(fruits)
print(type(fruits))

#list indexing

fruits = ["pear" , "melon" , "orange" , 10 , 20 , 30 , 40]
print(fruits[2])
print(type(fruits))

#negative list indexing
fruits = ["pear" , "melon" , "orange" , 10 , 20 , 30 ,40]
print(fruits[-2])
print(type(fruits))